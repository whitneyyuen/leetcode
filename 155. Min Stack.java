class MinStack {
    Deque<Integer> stack;

    public MinStack() {
        stack = new ArrayDeque<Integer>();
    }
    
    public void push(int val) {
        stack.push(val);
    }
    
    public void pop() {
        stack.pop();
    }
    
    public int top() {
        return stack.peek();
    }
    
    public int getMin() {
        int min = stack.element();
        for (int s: stack) {
            if (s<min) {
                min = s;
            }
        }
        return min;
    }
}

